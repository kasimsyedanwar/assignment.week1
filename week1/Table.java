package assignments.week1;

public class Table {
	public static void main(String[] args) {
		//declare a integer variable
		int a = 10;
		//iterate the integer value using for loop from 1 to 10
		for (int i = 1; i <=10; i++) {
			int b = a*i;
			//print the values
			System.out.println(a + "*"+ i + "="+b);
		}
		
	}

}
